use rand::Rng;
use serde::{Deserialize, Serialize};

/// the Word struct represent a struct of words to be malformed
#[derive(Debug, Serialize, Deserialize, Clone)]
pub struct Word {
    /// the original word
    word: String,
    /// meaning of the word
    meaning: String,
}

#[derive(Debug, Serialize, Deserialize, Clone)]
pub struct Jumble {
    /// the original word
    pub word: String,
    /// meaning of the word
    pub meaning: String,
    /// the scrabbled word
    pub scrabble: String,
}

impl Word {
    pub fn from(word: &str, meaning: &str) -> Self {
        Self {
            word: word.to_string(),
            meaning: meaning.to_string(),
        }
    }
}

// the library contains all words to be Wordd, the meanings and the Wordd ones
